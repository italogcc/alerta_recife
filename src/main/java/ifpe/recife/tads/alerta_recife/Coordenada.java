package ifpe.recife.tads.alerta_recife;

public class Coordenada {
 
    double pontoX;
    double pontoY;

    public Coordenada() {
        
    }

    public Coordenada(double pontoX, double pontoY) {
        this.pontoX = pontoX;
        this.pontoY = pontoY;
    }

    public double getPontoX() {
        return pontoX;
    }

    public void setPontoX(double pontoX) {
        this.pontoX = pontoX;
    }

    public double getPontoY() {
        return pontoY;
    }

    public void setPontoY(double pontoY) {
        this.pontoY = pontoY;
    }
    
    
    
}
